
public class NestedIfElse {

	public static void testingIfElse() {
		int x = 50;

		if (x < 100) {
			if (x == 20) {
				System.out.println("Print x value");

			} else if (x == 50) {
				System.out.println("Print x value");

			}

		} else if (x <= 50) {
			if (x == 20) {
				System.out.println("Print x value");
			} else if (x == 50) {
				System.out.println("Print x value");
			} else {
				System.out.println("dont print x value");
			}

		}

	}
	
	

}
